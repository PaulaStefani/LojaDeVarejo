class Produto(nome: String, categoria: String, valor: String, quantidadeEmEstoque: String) {
 var nome: String
    var categoria: String
    var valor: String
    var quantidadeEmEstoque: String
init{
    this.categoria=categoria
    this.valor=valor
    this.quantidadeEmEstoque=quantidadeEmEstoque
    this.nome=nome
}
}